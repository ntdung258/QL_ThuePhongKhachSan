    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String maNV = txtMaNV.getText().trim();
        String tenNV = txtTenNV.getText().trim();
        boolean gioiTinh = false;
        String ngaySinh = txtNgaySinh.getText().trim();
        String sDT = txtSDT.getText().trim();

        String err = "";

        if (maNV.length() == 0) {
            err += "\nChưa nhập mã nhân viên";
        } else {
            Pattern pat = Pattern.compile("^[NV]{2}+[0-9]{2,}$");
            if (!pat.matcher(maNV).find()) {
                err += "\nChưa đúng định dạng NV**";
            }
            boolean checkMaNV = new NhanVienDaoImp().checkMaNV(maNV);
            if (checkMaNV) {
                err += "\nMã nhân viên đã tồn tại ";
            }
        }

        if (tenNV.length() == 0) {
            err += "\nChưa nhập tên nhân viên";
        }

        if (!rdNam.isSelected() && !rdNu.isSelected()) {
            err += "\nChưa chọn giới tính";
        } else {
            if (rdNam.isSelected()) {
                gioiTinh = true;
            } else {
                gioiTinh = false;
            }
        }

        Date exNgaySinh = null;
        if (ngaySinh.length() == 0) {
            err += "\nChưa nhập ngày sinh";
        } else {
            SimpleDateFormat sf = new SimpleDateFormat("dd/MM/yyyy");
            try {
                exNgaySinh = sf.parse(ngaySinh);
            } catch (ParseException ex) {
                err += "Kiểu ngày tháng năm chưa đúng định dạng dd/MM/yyyy";
            }
        }

        if (sDT.length() == 0) {
            err += "\nChưa nhập số điện thoại";
        } else {
            Pattern pat = Pattern.compile("^[0-9]{10,15}$");
            if (!pat.matcher(sDT).find()) {
                err += "\nSố điện thoại từ 10 đến 15 số";
            }
        }

        if (err.length() == 0) {
            NhanVien nv = new NhanVien();
            nv.setMaNV(maNV);
            nv.setTenNV(tenNV);
            nv.setGioiTinh(gioiTinh);
            nv.setNgaySinh(exNgaySinh);
            nv.setSDT(sDT);
            boolean insertNhanVien = new NhanVienDaoImp().insertNhanVien(nv);
            if (insertNhanVien) {
                JOptionPane.showMessageDialog(this, "Thêm mới thành công");
                btnLoadAll.doClick();
                btnReset.doClick();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm mới thất bại");
            }
        } else {
            JOptionPane.showMessageDialog(this, err);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnFindByIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindByIdActionPerformed
        // TODO add your handling code here:
        txtMaNV.setEditable(false);
        String strMaNV = JOptionPane.showInputDialog("Nhập mã nhân viên cần tìm:");
        if (strMaNV != null && strMaNV.length() > 0) {
            String maNV = String.valueOf(strMaNV);
            NhanVien maNV1 = new NhanVienDaoImp().getMaNV(maNV);
            if (maNV1 != null) {
                txtMaNV.setText(maNV1.getMaNV() + "");
                txtTenNV.setText(maNV1.getTenNV() + "");
                if (maNV1.isGioiTinh() == true) {
                    rdNam.setSelected(rootPaneCheckingEnabled);
                } else {
                    rdNu.setSelected(rootPaneCheckingEnabled);
                }
                txtNgaySinh.setText(common.Common.FomatDate(maNV1.getNgaySinh()) + "");
                txtSDT.setText(maNV1.getSDT() + "");
            } else {
                JOptionPane.showMessageDialog(this, "Không có nhân viên nào mã: " + strMaNV);
            }
        }
    }//GEN-LAST:event_btnFindByIdActionPerformed

