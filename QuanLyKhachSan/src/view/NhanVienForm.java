    private void btnFindByNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindByNameActionPerformed
        // TODO add your handling code here:
        String strTenNV = JOptionPane.showInputDialog("Nhập tên nhân viên cần tìm:");

        List<NhanVien> nhanVienByName = new NhanVienDaoImp().getNhanVienByName(strTenNV);
        if (nhanVienByName == null || nhanVienByName.size()==0) {
            JOptionPane.showMessageDialog(this, "Không có nhân viên nào tên " + strTenNV);
        }else {
            defaultTableModel.setRowCount(0);
            for (NhanVien nhanVien : nhanVienByName) {
                Object[] objfnv = {nhanVien.getMaNV(), nhanVien.getTenNV(),
                    Common.GioiTinh(nhanVien.isGioiTinh()), Common.FomatDate(nhanVien.getNgaySinh()), nhanVien.getSDT()};
                defaultTableModel.addRow(objfnv);
            }
        }

    }//GEN-LAST:event_btnFindByNameActionPerformed

